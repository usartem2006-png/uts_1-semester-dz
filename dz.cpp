#include <iostream>
using namespace std;

// Функция проверки, является ли число степенью пятерки
bool IsPower5(int K) {
    if (K <= 0) {
        return false;  // отрицательные числа и ноль не могут быть степенью пятерки
    }

    // Делим число на 5 пока возможно
    while (K > 1) {
        if (K % 5 != 0) {
            return false;  // если не делится на 5 без остатка - не степень пятерки
        }
        K = K / 5;
    }

    return true;  // если дошли до 1 - это степень пятерки
}

int main() {
    int numbers[10];  // массив из 10 чисел
    int count = 0;    // счетчик степеней пятерки

    cout << "Введите 10 целых положительных чисел:" << endl;

    // Вводим 10 чисел
    for (int i = 0; i < 10; i++) {
        cout << "Число " << (i + 1) << ": ";
        cin >> numbers[i];
    }

    // Проверяем каждое число
    cout << "\nРезультаты проверки:" << endl;
    for (int i = 0; i < 10; i++) {
        if (IsPower5(numbers[i])) {
            cout << numbers[i] << " - является степенью пятерки" << endl;
            count++;
        } else {
            cout << numbers[i] << " - НЕ является степенью пятерки" << endl;
        }
    }

    // Выводим общее количество
    cout << "\nКоличество степеней пятерки в наборе: " << count << endl;

    return 0;
}
