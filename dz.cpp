#include <iostream>
using namespace std;

// Простая функция расчета
double calculateY(double x) {
    if (x > 3) {
        // y = 2x - 10
        return 2 * x - 10;
    }
    else if (x == 3) {
        // y = 10
        return 10;
    }
    else {
        // y = (2x + 1)² - 1
        double temp = 2 * x + 1;
        return temp * temp - 1;
    }
}

int main() {
    double x;

    cout << "Программа вычисляет y = f(x)" << endl;
    cout << "Введите x: ";
    cin >> x;

    double result = calculateY(x);

    cout << "Результат: y = " << result << endl;

    return 0;
}
