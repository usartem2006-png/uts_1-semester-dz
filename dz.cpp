// Функция для вычисления длины отрезка между двумя точками
double Leng(double xA, double yA, double xB, double yB) {
    double dx = xA - xB;  // разница по x
    double dy = yA - yB;  // разница по y
    return sqrt(dx * dx + dy * dy);  // формула длины отрезка
}

